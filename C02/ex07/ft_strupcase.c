char	*ft_strupcase(char *str)
{
	int		i;
	char	is_lower;

	i = 0;
	while (str[i] != '\0')
	{
		is_lower = (str[i] >= 'a' && str[i] <= 'z');
		if (is_lower)
		{
			str[i] -= 32;
		}
		i++;
	}
	return (str);
}
