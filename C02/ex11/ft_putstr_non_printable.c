#include <stdio.h>
#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	print_hexa(unsigned char a)
{
	char	*t;

	t = "0123456789abcdef";
	ft_putchar(t[a / 16]);
	ft_putchar(t[a % 16]);
}

void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 32 && str[i] <= 126)
		{
			write(1, &str[i], 1);
		}
		else
		{
			write(1, "\\", 1);
			print_hexa(str[i]);
		}
		i++;
	}
}
